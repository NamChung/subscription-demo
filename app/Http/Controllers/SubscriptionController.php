     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\JsonResponse
     */
    public function store(SubscriptionStoreRequest $request)
    {
        $planId = $request->get('id');
        $name = $request->get('name');
        $user = $request->user();
